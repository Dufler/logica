package it.ltc.logica.trasporti.controller.preventivi;

import it.ltc.logica.common.calcolo.algoritmi.MVoceDiListino.Scopo;

/**
 * Rappresenta in forma sintetica i totali calcolati a partire da un listino.
 * @author Damiano Bellucci - damiano.bellucci@gmail.com
 *
 */
public class TotaleListino {
	
	private final Integer idListino;
	private final Scopo scopo;
 
	private String nome;
	private String tipoListino;
	private double totale;
	private double nolo;
	private double contrassegno;
	private double extra;
	
	private boolean costoMinore;
	private boolean ricavoMaggiore;
	
	public TotaleListino(Integer idListino, Scopo scopo) {
		this.idListino = idListino;
		this.scopo = scopo;
	}

	public Integer getIdListino() {
		return idListino;
	}

	public Scopo getScopo() {
		return scopo;
	}

	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getTipoListino() {
		return tipoListino;
	}

	public void setTipoListino(String tipoListino) {
		this.tipoListino = tipoListino;
	}

	public double getTotale() {
		return totale;
	}
	
	public void setTotale(double totale) {
		this.totale = totale;
	}
	
	public double getNolo() {
		return nolo;
	}
	
	public void setNolo(double nolo) {
		this.nolo = nolo;
	}
	
	public double getContrassegno() {
		return contrassegno;
	}
	
	public void setContrassegno(double contrassegno) {
		this.contrassegno = contrassegno;
	}
	
	public double getExtra() {
		return extra;
	}
	
	public void setExtra(double extra) {
		this.extra = extra;
	}

	public boolean isCostoMinore() {
		return costoMinore;
	}

	public void setCostoMinore(boolean costoMinore) {
		this.costoMinore = costoMinore;
	}

	public boolean isRicavoMaggiore() {
		return ricavoMaggiore;
	}

	public void setRicavoMaggiore(boolean ricavoMaggiore) {
		this.ricavoMaggiore = ricavoMaggiore;
	}
	
}
